run_6() {
  local name="P6: Istio + DNS-01 domain change (stale-secret cleanup)"
  local ts; ts=$(date +%Y%m%d-%H%M%S)
  local logdir="$LOG_DIR/p6-istio-domain-change-$ts"
  mkdir -p "$logdir"
  local log="$logdir/run.log"
  _reset_results

  _section "$name"

  if [[ -z "$_domain" || -z "$_le_email" || -z "$_hosted_zone" ]]; then
    _warn "Skipping $name — requires langsmith_domain, letsencrypt_email, cert_manager_hosted_zone_id"
    return 0
  fi

  # Derive an alt domain by prepending "alt." — assumes same Route 53 zone
  local _alt_domain="alt.${_domain}"
  _warn "This permutation applies with domain=${_domain} then changes to alt-domain=${_alt_domain}"
  _warn "The alt domain must be in the same Route 53 zone: ${_hosted_zone}"

  _step "Phase A: initial apply with ${_domain}"
  _write_override "tls_certificate_source   = \"none\"
langsmith_domain         = \"${_domain}\"
letsencrypt_email        = \"${_le_email}\"
enable_envoy_gateway     = false
enable_istio_gateway     = true
create_cert_manager_irsa = true
cert_manager_hosted_zone_id = \"${_hosted_zone}\""

  _helm_uninstall "$log"
  _istio_uninstall "$log"
  _tf_destroy "$log"
  _tf_apply "$log"
  _kubeconfig
  _istio_install "$log"
  _helm_deploy "$log"

  # Verify initial cert
  local initial_san
  initial_san=$(kubectl get certificate langsmith-tls -n istio-system \
    -o jsonpath='{.spec.dnsNames[0]}' 2>/dev/null || echo "")
  _assert "Phase A: cert SAN is ${_domain}" "$([[ "$initial_san" == "$_domain" ]] && echo true || echo false)" "$initial_san"

  local old_secret_uid
  old_secret_uid=$(kubectl get secret langsmith-tls -n istio-system \
    -o jsonpath='{.metadata.uid}' 2>/dev/null || echo "")
  _assert "Phase A: TLS secret exists" "$([[ -n "$old_secret_uid" ]] && echo true || echo false)"

  _step "Phase B: changing domain to ${_alt_domain}"
  _write_override "tls_certificate_source   = \"none\"
langsmith_domain         = \"${_alt_domain}\"
letsencrypt_email        = \"${_le_email}\"
enable_envoy_gateway     = false
enable_istio_gateway     = true
create_cert_manager_irsa = true
cert_manager_hosted_zone_id = \"${_hosted_zone}\""

  # Re-apply — triggers_replace on langsmith_domain causes re-run of cert + gateway resources
  if [[ "$DRY_RUN" != "true" ]]; then
    _tf apply -auto-approve >> "$log" 2>&1
    _ok "Re-applied with new domain"
  fi

  # Verify old secret was deleted (uid changed = new cert issued)
  _step "Waiting for new certificate (up to 5 min)..."
  for i in $(seq 1 30); do
    local ready
    ready=$(kubectl get certificate langsmith-tls -n istio-system \
      -o jsonpath='{.status.conditions[?(@.type=="Ready")].status}' 2>/dev/null || echo "")
    [[ "$ready" == "True" ]] && break
    sleep 10
  done

  local new_san
  new_san=$(kubectl get certificate langsmith-tls -n istio-system \
    -o jsonpath='{.spec.dnsNames[0]}' 2>/dev/null || echo "")
  _assert "Phase B: cert SAN updated to ${_alt_domain}" "$([[ "$new_san" == "$_alt_domain" ]] && echo true || echo false)" "$new_san"

  local new_secret_uid
  new_secret_uid=$(kubectl get secret langsmith-tls -n istio-system \
    -o jsonpath='{.metadata.uid}' 2>/dev/null || echo "")
  _assert "Phase B: TLS secret is a new object (old deleted)" \
    "$([[ "$new_secret_uid" != "$old_secret_uid" ]] && echo true || echo false)" \
    "old=$old_secret_uid new=$new_secret_uid"

  local cert_ready
  cert_ready=$(kubectl get certificate langsmith-tls -n istio-system \
    -o jsonpath='{.status.conditions[?(@.type=="Ready")].status}' 2>/dev/null || echo "")
  _assert "Phase B: new cert READY=True" "$([[ "$cert_ready" == "True" ]] && echo true || echo false)"

  _assert_k8s "Gateway updated to new domain host" \
    "kubectl get gateway langsmith-gateway -n langsmith -o jsonpath='{.spec.servers[0].hosts[0]}'" \
    "$_alt_domain"

  _print_results "$name" "$ts"
}

run_7() {
  local name="P7: terraform destroy with DNS-01 active (no orphaned resources)"
  local ts; ts=$(date +%Y%m%d-%H%M%S)
  local logdir="$LOG_DIR/p7-destroy-dns01-$ts"
  mkdir -p "$logdir"
  local log="$logdir/run.log"
  _reset_results

  _section "$name"

  if [[ -z "$_domain" || -z "$_le_email" || -z "$_hosted_zone" ]]; then
    _warn "Skipping $name — requires langsmith_domain, letsencrypt_email, cert_manager_hosted_zone_id"
    return 0
  fi

  # P7 is self-contained: it sets up DNS-01 from scratch, then destroys and
  # verifies no orphaned resources remain.
  #
  # The destroy provisioners on terraform_data resources (ClusterIssuer,
  # Certificate, Gateway) only fire when Terraform has those resources in
  # state. This requires a full apply in THIS worktree before destroy — we
  # cannot rely on state from a prior run or a different worktree (BUG-06).
  _write_override "tls_certificate_source   = \"none\"
langsmith_domain         = \"${_domain}\"
letsencrypt_email        = \"${_le_email}\"
enable_envoy_gateway     = false
enable_istio_gateway     = true
create_cert_manager_irsa = true
cert_manager_hosted_zone_id = \"${_hosted_zone}\""

  _step "Phase 1: Apply infrastructure with DNS-01 (populates state for destroy)..."
  _helm_uninstall "$log"
  _istio_uninstall "$log"
  _tf_apply "$log"
  _kubeconfig
  _istio_install "$log"
  # Taint Certificate + Gateway resources so pass 2 recreates them with Istio ready.
  if [[ "$DRY_RUN" != "true" ]]; then
    _step "Tainting Certificate + Gateway resources for pass 2"
    _cert_res=$(_tf state list 2>/dev/null | grep "terraform_data.langsmith_certificate" || echo "")
    _gw_res=$(_tf state list 2>/dev/null | grep "terraform_data.istio_gateway_tls" || echo "")
    [[ -n "$_cert_res" ]] && terraform -chdir="$INFRA_DIR" taint "$_cert_res" 2>/dev/null || true
    [[ -n "$_gw_res" ]] && terraform -chdir="$INFRA_DIR" taint "$_gw_res" 2>/dev/null || true
    _ok "Resources tainted for pass 2"
  fi
  _step "terraform apply (pass 2 — Certificate + Gateway provisioners)"
  [[ "$DRY_RUN" != "true" ]] && _tf apply -auto-approve >> "$log" 2>&1 && _ok "Pass 2 applied"

  _step "Phase 2: Running terraform destroy (with DNS-01 active)..."
  _helm_uninstall "$log"
  _tf_destroy "$log"
  _istio_uninstall "$log"

  # Post-destroy assertions — nothing should remain
  _step "Asserting no orphaned Kubernetes resources..."
  _assert_k8s_absent "ClusterIssuer letsencrypt-prod deleted" \
    "kubectl get clusterissuer letsencrypt-prod -o name 2>/dev/null"
  _assert_k8s_absent "Certificate langsmith-tls deleted from istio-system" \
    "kubectl get certificate langsmith-tls -n istio-system -o name 2>/dev/null"
  _assert_k8s_absent "TLS secret langsmith-tls deleted from istio-system" \
    "kubectl get secret langsmith-tls -n istio-system -o name 2>/dev/null"

  _step "Asserting no orphaned IAM resources..."
  local role_name="${_cluster_name}-cert-manager"
  _assert_k8s_absent "cert-manager IAM role deleted" \
    "aws iam get-role --role-name $role_name --query 'Role.RoleName' --output text 2>/dev/null"

  local policy_name="${_cluster_name}-cert-manager-route53"
  _assert_k8s_absent "cert-manager IAM policy deleted" \
    "aws iam list-policies --scope Local --query \"Policies[?PolicyName=='$policy_name'].PolicyName\" --output text 2>/dev/null"

  _print_results "$name" "$ts"
}

run_8() {
  local name="P8: Envoy Gateway + tls=none"
  local ts; ts=$(date +%Y%m%d-%H%M%S)
  local logdir="$LOG_DIR/p8-envoy-none-$ts"
  mkdir -p "$logdir"
  local log="$logdir/run.log"
  _reset_results

  _section "$name"

  if [[ -z "$_domain" ]]; then
    _warn "Skipping $name — langsmith_domain not set (needed for Gateway hosts field)"
    return 0
  fi

  _write_override "tls_certificate_source  = \"none\"
langsmith_domain        = \"${_domain}\"
enable_envoy_gateway    = true
enable_istio_gateway    = false
create_cert_manager_irsa = false"

  _helm_uninstall "$log"
  _tf_destroy "$log"
  _tf_apply "$log"
  _kubeconfig
  _helm_deploy "$log"

  # Wait for GatewayClass to be accepted and Gateway to be programmed.
  # The terraform_data provisioner creates these immediately after Helm install
  # but the Envoy Gateway controller needs ~30-60s to reconcile them.
  _step "Waiting for GatewayClass 'eg' to be Accepted (up to 3 min)..."
  if [[ "$DRY_RUN" != "true" ]]; then
    for i in $(seq 1 18); do
      local _gc_accepted
      _gc_accepted=$(kubectl get gatewayclass eg \
        -o jsonpath='{.status.conditions[?(@.type=="Accepted")].status}' 2>/dev/null || echo "")
      [[ "$_gc_accepted" == "True" ]] && break
      sleep 10
    done
  fi

  _step "Waiting for Gateway 'langsmith-gateway' to be Programmed (up to 3 min)..."
  if [[ "$DRY_RUN" != "true" ]]; then
    for i in $(seq 1 18); do
      local _gw_prog
      _gw_prog=$(kubectl get gateway langsmith-gateway -n langsmith \
        -o jsonpath='{.status.conditions[?(@.type=="Programmed")].status}' 2>/dev/null || echo "")
      [[ "$_gw_prog" == "True" ]] && break
      sleep 10
    done
  fi

  _step "Running assertions..."

  # Control plane
  _assert_k8s "envoy-gateway deployment ready" \
    "kubectl get deployment envoy-gateway -n envoy-gateway-system \
       -o jsonpath='{.status.readyReplicas}'" \
    "$(kubectl get deployment envoy-gateway -n envoy-gateway-system \
         -o jsonpath='{.spec.replicas}' 2>/dev/null || echo "1")"

  # GatewayClass
  _assert_k8s "GatewayClass 'eg' accepted" \
    "kubectl get gatewayclass eg \
       -o jsonpath='{.status.conditions[?(@.type==\"Accepted\")].status}'" \
    "True"

  # Gateway
  _assert_k8s "Gateway langsmith-gateway exists in langsmith ns" \
    "kubectl get gateway langsmith-gateway -n langsmith \
       -o jsonpath='{.metadata.name}'" \
    "langsmith-gateway"

  _assert_k8s "Gateway has HTTP listener on port 80" \
    "kubectl get gateway langsmith-gateway -n langsmith \
       -o jsonpath='{.spec.listeners[?(@.port==80)].name}'" \
    "http"

  # NLB provisioned by Envoy Gateway — wait up to 5 min for hostname
  _step "Waiting for Envoy Gateway NLB hostname (up to 5 min)..."
  local nlb_host=""
  if [[ "$DRY_RUN" != "true" ]]; then
    for i in $(seq 1 30); do
      nlb_host=$(kubectl get svc -n envoy-gateway-system \
        -l "gateway.envoyproxy.io/owning-gateway-name=langsmith-gateway" \
        -o jsonpath='{.items[0].status.loadBalancer.ingress[0].hostname}' 2>/dev/null || echo "")
      [[ -n "$nlb_host" ]] && break
      sleep 10
    done
  fi
  _assert "Envoy Gateway NLB hostname provisioned" \
    "$([[ -n "$nlb_host" ]] && echo true || echo false)" "$nlb_host"

  # HTTPRoute
  _assert_k8s_exists "HTTPRoute langsmith exists" \
    "kubectl get httproute -n langsmith -o name 2>/dev/null | grep langsmith | head -1"

  # Traffic — wait up to 3 min for NLB to become traffic-ready
  # (hostname appears in AWS before the LB finishes health-check probing)
  _step "Waiting for NLB to serve traffic on http://${nlb_host}/api/v1/health (up to 3 min)..."
  if [[ "$DRY_RUN" != "true" && -n "$nlb_host" ]]; then
    for i in $(seq 1 18); do
      local _hc
      _hc=$(curl -sk -o /dev/null -w "%{http_code}" --max-time 10 \
        "http://${nlb_host}/api/v1/health" 2>/dev/null || echo "000")
      [[ "$_hc" == "200" ]] && break
      sleep 10
    done
  fi
  _assert_http "GET http://${nlb_host}/api/v1/health → 200" \
    "http://${nlb_host}/api/v1/health"

  _assert_k8s_absent "No ClusterIssuer letsencrypt-prod" \
    "kubectl get clusterissuer letsencrypt-prod -o name 2>/dev/null"
  _assert_k8s_absent "No Istio istiod deployment" \
    "kubectl get deployment istiod -n istio-system -o name 2>/dev/null"

  _print_results "$name" "$ts"
}

