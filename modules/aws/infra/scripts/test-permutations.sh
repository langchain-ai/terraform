run_5() {
  local name="P5: Istio + Let's Encrypt DNS-01 (Route 53 IRSA)"
  local ts; ts=$(date +%Y%m%d-%H%M%S)
  local logdir="$LOG_DIR/p5-istio-dns01-$ts"
  mkdir -p "$logdir"
  local log="$logdir/run.log"
  _reset_results

  _section "$name"

  if [[ -z "$_domain" || -z "$_le_email" || -z "$_hosted_zone" ]]; then
    _warn "Skipping $name — langsmith_domain, letsencrypt_email, or cert_manager_hosted_zone_id not set"
    return 0
  fi

  _write_override "tls_certificate_source   = \"none\"
langsmith_domain         = \"${_domain}\"
letsencrypt_email        = \"${_le_email}\"
enable_envoy_gateway     = false
enable_istio_gateway     = true
create_cert_manager_irsa = true
cert_manager_hosted_zone_id = \"${_hosted_zone}\""

  _helm_uninstall "$log"
  _istio_uninstall "$log"
  _tf_destroy "$log"
  _tf_apply "$log"
  _kubeconfig
  _istio_install "$log"
  # Taint Certificate + Gateway resources so pass 2 recreates them.
  # Required because _istio_uninstall removes istio-system (deleting the
  # Certificate/Gateway Kubernetes objects) but terraform state still marks
  # these terraform_data resources as current — without a taint they won't re-run.
  if [[ "$DRY_RUN" != "true" ]]; then
    _step "Tainting Certificate + Gateway resources for pass 2"
    _cert_res=$(_tf state list 2>/dev/null | grep "terraform_data.langsmith_certificate" || echo "")
    _gw_res=$(_tf state list 2>/dev/null | grep "terraform_data.istio_gateway_tls" || echo "")
    [[ -n "$_cert_res" ]] && terraform -chdir="$INFRA_DIR" taint "$_cert_res" 2>/dev/null || true
    [[ -n "$_gw_res" ]] && terraform -chdir="$INFRA_DIR" taint "$_gw_res" 2>/dev/null || true
    _ok "Resources tainted for pass 2"
  fi
  # NOTE: The Certificate and Gateway terraform_data provisioners require istio-system
  # to exist. They are tainted above so pass 2 re-runs them with Istio now ready.
  _step "terraform apply (pass 2 — Certificate + Gateway provisioners)"
  [[ "$DRY_RUN" != "true" ]] && _tf apply -auto-approve >> "$log" 2>&1 && _ok "Pass 2 applied"
  _helm_deploy "$log"

  _step "Running assertions..."

  # IAM assertions
  local irsa_arn
  irsa_arn=$(_tf output -raw cert_manager_irsa_role_arn 2>/dev/null || echo "")
  _assert "cert-manager IRSA role ARN in TF output" "$([[ -n "$irsa_arn" ]] && echo true || echo false)" "$irsa_arn"

  local role_name="${_cluster_name}-cert-manager"
  _assert_k8s_exists "cert-manager IAM role exists in AWS" \
    "aws iam get-role --role-name $role_name --query 'Role.RoleName' --output text 2>/dev/null"

  # cert-manager assertions
  _assert_k8s "cert-manager SA annotated with IRSA role" \
    "kubectl get sa cert-manager -n cert-manager -o jsonpath='{.metadata.annotations.eks\.amazonaws\.com/role-arn}'" \
    "$irsa_arn"

  _assert_k8s "ClusterIssuer letsencrypt-prod exists" \
    "kubectl get clusterissuer letsencrypt-prod -o jsonpath='{.metadata.name}'" \
    "letsencrypt-prod"
  _assert_k8s "ClusterIssuer uses dns01/route53 solver" \
    "kubectl get clusterissuer letsencrypt-prod -o jsonpath='{.spec.acme.solvers[0].dns01.route53.hostedZoneID}'" \
    "$_hosted_zone"

  # Certificate assertions
  _assert_k8s "Certificate langsmith-tls exists in istio-system" \
    "kubectl get certificate langsmith-tls -n istio-system -o jsonpath='{.metadata.name}'" \
    "langsmith-tls"
  local cert_ready
  cert_ready=$(kubectl get certificate langsmith-tls -n istio-system \
    -o jsonpath='{.status.conditions[?(@.type=="Ready")].status}' 2>/dev/null || echo "")
  _assert "Certificate langsmith-tls READY=True" "$([[ "$cert_ready" == "True" ]] && echo true || echo false)" "status=$cert_ready"

  _assert_k8s_exists "TLS secret langsmith-tls in istio-system" \
    "kubectl get secret langsmith-tls -n istio-system -o name 2>/dev/null"

  # Gateway assertions
  _assert_k8s "Gateway has HTTPS listener (port 443)" \
    "kubectl get gateway langsmith-gateway -n langsmith -o jsonpath='{.spec.servers[?(@.port.number==443)].port.name}'" \
    "https"
  _assert_k8s "Gateway uses credentialName langsmith-tls" \
    "kubectl get gateway langsmith-gateway -n langsmith -o jsonpath='{.spec.servers[?(@.port.number==443)].tls.credentialName}'" \
    "langsmith-tls"
  _assert_k8s "Gateway HTTP listener has httpsRedirect=true" \
    "kubectl get gateway langsmith-gateway -n langsmith -o jsonpath='{.spec.servers[?(@.port.number==80)].tls.httpsRedirect}'" \
    "true"

  # Traffic assertions
  _assert_http "GET https://${_domain}/api/v1/health → 200" "https://${_domain}/api/v1/health"
  _assert_http "GET http://${_domain}/ → 301 HTTPS redirect" "http://${_domain}/" "301"
  _assert_tls_issuer "TLS cert issued by Let's Encrypt" "https://${_domain}" "Let's Encrypt"

  # SAN check
  local cert_san
  cert_san=$(openssl s_client -connect "${_domain}:443" -servername "$_domain" </dev/null 2>/dev/null \
    | openssl x509 -noout -text 2>/dev/null | grep -A1 "Subject Alternative Name" | tail -1 || echo "")
  _assert "TLS cert SAN matches domain" "$([[ "$cert_san" == *"$_domain"* ]] && echo true || echo false)" "$cert_san"

  _print_results "$name" "$ts"
