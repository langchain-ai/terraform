# test-permutations.sh — Cycles through all cert-manager / gateway / TLS permutations.
#
# Each permutation writes a permutation.auto.tfvars that overrides ONLY the
# gateway/TLS variables — your terraform.tfvars (cluster, RDS, Redis, etc.)
# is never touched. Terraform's auto.tfvars load-order guarantees these
# overrides win.
#
# Usage:
#   ./infra/scripts/test-permutations.sh           # run all permutations
#   ./infra/scripts/test-permutations.sh 5         # run one permutation
#   ./infra/scripts/test-permutations.sh 5 6 7     # run a subset
#   ./infra/scripts/test-permutations.sh --dry-run # show plan only, no apply
#   ./infra/scripts/test-permutations.sh --skip-destroy 5  # skip destroy phase
#
# Permutations:
#   1  ALB only         tls=none
#   2  ALB + ACM        tls=acm  (needs langsmith_domain + NS delegation)
#   3  ALB + Let's Encrypt HTTP-01  tls=letsencrypt  (needs domain)
#   4  Istio Gateway    tls=none  (no TLS)
#   5  Istio + Let's Encrypt DNS-01 via Route 53 IRSA  (full cert-manager path)
#   6  Istio + DNS-01 domain change  (validate stale-secret deletion + re-issuance)
#   7  terraform destroy with DNS-01 active  (validate destroy provisioners)
#   8  Envoy Gateway    tls=none  (no TLS — validates feat/aws-envoy code path)
#
# Prerequisites (set in terraform.tfvars before running):
#   - name_prefix, environment, region
#   - postgres_source, redis_source (external or in-cluster)
#   - For permutations 2-6: langsmith_domain (must be Route 53 delegated)
#   - For permutations 2, 3, 5, 6: letsencrypt_email
#   - For permutations 5, 6, 7: cert_manager_hosted_zone_id
#   - For permutations 2-7: domain must be Route 53 NS-delegated
#
# Log files:
#   Each run writes full output to logs/permutation-<N>-<timestamp>/
#   Summary is printed to stdout and appended to logs/summary.log

set -euo pipefail
export AWS_PAGER=""

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
INFRA_DIR="$SCRIPT_DIR/.."
AWS_DIR="$INFRA_DIR/.."
HELM_DIR="$AWS_DIR/helm"
source "$SCRIPT_DIR/_common.sh"

OVERRIDE_FILE="$INFRA_DIR/permutation.auto.tfvars"
LOG_DIR="$SCRIPT_DIR/../logs"
SUMMARY_LOG="$LOG_DIR/summary.log"
ISTIO_VERSION="${ISTIO_VERSION:-1.24.3}"

# ── Arg parsing ───────────────────────────────────────────────────────────────

DRY_RUN=false
SKIP_DESTROY=false
RUN_ALL=false
SELECTED=()

for arg in "$@"; do
  case "$arg" in
    --dry-run)       DRY_RUN=true ;;
    --skip-destroy)  SKIP_DESTROY=true ;;
    all|"")          RUN_ALL=true ;;
    [1-8])           SELECTED+=("$arg") ;;
    *)               echo "Unknown argument: $arg" >&2; exit 1 ;;
  esac
done

[[ ${#SELECTED[@]} -eq 0 ]] && RUN_ALL=true

# ── Validate prerequisites ────────────────────────────────────────────────────

if [[ ! -f "$INFRA_DIR/terraform.tfvars" ]]; then
  echo "ERROR: terraform.tfvars not found. Copy terraform.tfvars.example and fill it in." >&2
  exit 1
fi

_region=$(_parse_tfvar "region") || { echo "ERROR: region not set in terraform.tfvars" >&2; exit 1; }
_name_prefix=$(_parse_tfvar "name_prefix") || { echo "ERROR: name_prefix not set in terraform.tfvars" >&2; exit 1; }
_environment=$(_parse_tfvar "environment") || { echo "ERROR: environment not set in terraform.tfvars" >&2; exit 1; }
_domain=$(_parse_tfvar "langsmith_domain") || _domain=""
_le_email=$(_parse_tfvar "letsencrypt_email") || _le_email=""
_hosted_zone=$(_parse_tfvar "cert_manager_hosted_zone_id") || _hosted_zone=""

_cluster_name="${_name_prefix}-${_environment}-eks"

mkdir -p "$LOG_DIR"

# ── Helpers ───────────────────────────────────────────────────────────────────

_section() { echo ""; printf '\033[1m══ %s ══\033[0m\n' "$1"; echo ""; }
_step()    { printf '  \033[0;36m→\033[0m  %s\n' "$1"; }
_ok()      { printf '  \033[32m✔\033[0m  %s\n' "$1"; }
_err()     { printf '  \033[31m✘\033[0m  %s\n' "$1"; }
_warn()    { printf '  \033[33m⚠\033[0m  %s\n' "$1"; }

_run() {
  # _run <log_file> <description> <cmd...>
  local logfile="$1"; shift
  local desc="$1"; shift
  _step "$desc"
  if [[ "$DRY_RUN" == "true" ]]; then
    echo "    [dry-run] would run: $*" | tee -a "$logfile"
    return 0
  fi
  if "$@" >> "$logfile" 2>&1; then
    _ok "$desc"
    return 0
  else
    local rc=$?
    _err "$desc (exit $rc — see $logfile)"
    return $rc
  fi
}

_write_override() {
  # Write permutation.auto.tfvars with the given content.
  # Terraform loads *.auto.tfvars after terraform.tfvars — these vars win.
  local content="$1"
  cat > "$OVERRIDE_FILE" <<EOF
# Auto-generated by test-permutations.sh — DO NOT EDIT MANUALLY
# Override file for permutation testing. Deleted after each run.
$content
EOF
  _step "Wrote permutation.auto.tfvars"
  [[ "$DRY_RUN" == "true" ]] && cat "$OVERRIDE_FILE" | sed 's/^/    /'
}

_cleanup_override() {
  rm -f "$OVERRIDE_FILE"
}

_tf() {
  # Run terraform in INFRA_DIR
  terraform -chdir="$INFRA_DIR" "$@"
}

_kubeconfig() {
  _step "Updating kubeconfig for $_cluster_name"
  [[ "$DRY_RUN" == "true" ]] && return 0
  aws eks update-kubeconfig --name "$_cluster_name" --region "$_region" >/dev/null 2>&1
}

_helm_uninstall() {
  local logfile="$1"
  _step "Uninstalling Helm releases (langsmith, langgraph-dataplane)"
  [[ "$DRY_RUN" == "true" ]] && return 0
  {
    helm uninstall langsmith -n langsmith 2>/dev/null || true
    helm uninstall langgraph-dataplane -n langsmith-agents 2>/dev/null || true
    # Wait for namespace to drain before destroy
    kubectl delete namespace langsmith langsmith-agents 2>/dev/null || true
    kubectl wait --for=delete namespace/langsmith --timeout=120s 2>/dev/null || true
  } >> "$logfile" 2>&1
}

_tf_destroy() {
  local logfile="$1"
  [[ "$SKIP_DESTROY" == "true" ]] && { _warn "Skipping destroy (--skip-destroy)"; return 0; }
  _step "terraform destroy -auto-approve"
  [[ "$DRY_RUN" == "true" ]] && return 0

  # Pre-remove k8s-provider resources from state before destroying AWS infra.
  # When EKS nodes drain during destroy, the kubernetes/helm providers lose
  # connectivity and cause Terraform to error on Helm releases and k8s resources
  # even though the underlying AWS resources are already gone. Removing them from
  # state first lets terraform destroy proceed cleanly against just the AWS layer.
  local _k8s_resources
  _k8s_resources=$(_tf state list 2>/dev/null | grep -E \
    "helm_release\.|kubernetes_namespace\.|kubernetes_storage_class\.|kubernetes_secret\.|terraform_data\.letsencrypt|terraform_data\.istio|time_sleep\." || true)
  if [[ -n "$_k8s_resources" ]]; then
    _warn "Pre-removing k8s-provider state entries to avoid connectivity errors during destroy"
    echo "$_k8s_resources" | xargs -I{} _tf state rm "{}" >> "$logfile" 2>&1 || true
  fi

  _tf destroy -auto-approve >> "$logfile" 2>&1
  _ok "Infrastructure destroyed"
}

_tf_apply() {
  local logfile="$1"
  _step "terraform apply -auto-approve"
  [[ "$DRY_RUN" == "true" ]] && return 0
  _tf init -upgrade >> "$logfile" 2>&1
  _tf apply -auto-approve >> "$logfile" 2>&1
  _ok "Infrastructure applied"
}

_helm_deploy() {
  local logfile="$1"
  _step "init-values + helm deploy"
  [[ "$DRY_RUN" == "true" ]] && return 0
  "$HELM_DIR/scripts/init-values.sh" >> "$logfile" 2>&1
  "$HELM_DIR/scripts/deploy.sh" >> "$logfile" 2>&1
  _ok "LangSmith deployed"
}

# ── Istio install/uninstall helpers ───────────────────────────────────────────

_istio_install() {
  local logfile="$1"
  _step "Installing Istio $ISTIO_VERSION (istiod + ingressgateway)"
  [[ "$DRY_RUN" == "true" ]] && return 0
  {
    helm repo add istio https://istio-release.storage.googleapis.com/charts --force-update
    helm repo update
    helm upgrade --install istio-base istio/base \
      --namespace istio-system --create-namespace --version "$ISTIO_VERSION" --wait
    helm upgrade --install istiod istio/istiod \
      --namespace istio-system --version "$ISTIO_VERSION" --wait
    # Install ingressgateway on public subnets (internet-facing NLB)
    _public_subnets=$(aws ec2 describe-subnets \
      --region "$_region" \
      --filters "Name=tag:kubernetes.io/cluster/${_cluster_name},Values=shared,owned" \
                "Name=mapPublicIpOnLaunch,Values=true" \
      --query 'Subnets[*].SubnetId' \
      --output text | tr '\t\n' ',,' | sed 's/,*$//' | sed 's/,/\\,/g')
    if [[ -n "$_public_subnets" ]]; then
      helm upgrade --install istio-ingressgateway istio/gateway \
        --namespace istio-system --version "$ISTIO_VERSION" \
        --skip-schema-validation \
        --set "service.annotations.service\.beta\.kubernetes\.io/aws-load-balancer-scheme=internet-facing" \
        --set "service.annotations.service\.beta\.kubernetes\.io/aws-load-balancer-subnets=${_public_subnets}" \
        --wait
    else
      helm upgrade --install istio-ingressgateway istio/gateway \
        --namespace istio-system --version "$ISTIO_VERSION" \
        --skip-schema-validation --wait
    fi
  } >> "$logfile" 2>&1
  _ok "Istio installed"
}

_istio_uninstall() {
  local logfile="$1"
  _step "Uninstalling Istio"
  [[ "$DRY_RUN" == "true" ]] && return 0
  {
    helm uninstall istio-ingressgateway -n istio-system 2>/dev/null || true
    helm uninstall istiod -n istio-system 2>/dev/null || true
    helm uninstall istio-base -n istio-system 2>/dev/null || true
    kubectl delete namespace istio-system 2>/dev/null || true
    kubectl wait --for=delete namespace/istio-system --timeout=120s 2>/dev/null || true
  } >> "$logfile" 2>&1
  _ok "Istio uninstalled"
}

_istio_gateway_http() {
  # Create an HTTP-only Istio Gateway in the langsmith namespace.
  local domain="$1"
  local logfile="$2"
  _step "Creating Istio Gateway (HTTP-only) for $domain"
  [[ "$DRY_RUN" == "true" ]] && return 0
  kubectl apply -f - >> "$logfile" 2>&1 <<EOF
apiVersion: networking.istio.io/v1beta1
kind: Gateway
metadata:
  name: langsmith-gateway
  namespace: langsmith
spec:
  selector:
    istio: ingressgateway
  servers:
  - port:
      number: 80
      name: http
      protocol: HTTP
    hosts:
    - "${domain}"
EOF
  _ok "Istio Gateway created"
}

_nlb_hostname() {
  kubectl get svc istio-ingressgateway -n istio-system \
    -o jsonpath='{.status.loadBalancer.ingress[0].hostname}' 2>/dev/null || echo ""
}

# ── Assertion helpers ─────────────────────────────────────────────────────────

_PASS=0
_FAIL=0
_RESULTS=()

_assert() {
  local name="$1"
  local ok="$2"
  local detail="${3:-}"
  if [[ "$ok" == "true" ]]; then
    _PASS=$((_PASS + 1))
    _RESULTS+=("  ✔  $name")
  else
    _FAIL=$((_FAIL + 1))
    _RESULTS+=("  ✗  $name${detail:+  ($detail)}")
  fi
}

_reset_results() { _PASS=0; _FAIL=0; _RESULTS=(); }

_assert_http() {
  local name="$1"
  local url="$2"
  local expected="${3:-200}"
  local code
  code=$(curl -sk -o /dev/null -w "%{http_code}" --max-time 15 "$url" 2>/dev/null || echo "000")
  [[ "$code" == "$expected" ]] \
    && _assert "$name" true "HTTP $code" \
    || _assert "$name" false "expected $expected, got $code"
}

_assert_tls_issuer() {
  local name="$1"
  local url="$2"
  local expect_pattern="$3"
  local issuer
  issuer=$(curl -sv --max-time 10 "$url" 2>&1 | grep -i "issuer:" | head -1 || echo "")
  if echo "$issuer" | grep -qi "$expect_pattern"; then
    _assert "$name" true "$issuer"
  else
    _assert "$name" false "issuer '$issuer' does not match '$expect_pattern'"
  fi
}

_assert_k8s() {
  local name="$1"
  local cmd="$2"
  local expected="$3"
  local actual
  actual=$(eval "$cmd" 2>/dev/null || echo "")
  [[ "$actual" == "$expected" ]] \
    && _assert "$name" true \
    || _assert "$name" false "expected '$expected', got '$actual'"
}

_assert_k8s_exists() {
  local name="$1"
  local cmd="$2"
  local result
  result=$(eval "$cmd" 2>/dev/null || echo "")
  [[ -n "$result" ]] \
    && _assert "$name" true \
    || _assert "$name" false "not found"
}

_assert_k8s_absent() {
  local name="$1"
  local cmd="$2"
  local result
  result=$(eval "$cmd" 2>/dev/null || echo "")
  [[ -z "$result" ]] \
    && _assert "$name" true \
    || _assert "$name" false "expected absent, found: $result"
}

_print_results() {
  local perm_name="$1"
  local ts="$2"
  echo ""
  printf '\033[1m── Results: %s ──\033[0m\n' "$perm_name"
  for r in "${_RESULTS[@]}"; do echo "$r"; done
  echo ""
  if [[ $_FAIL -eq 0 ]]; then
    printf '  \033[32mPASSED\033[0m  %d/%d tests\n' "$_PASS" "$((_PASS + _FAIL))"
    echo "  PASSED  $_PASS/$((_PASS+_FAIL))  [$perm_name]  $ts" >> "$SUMMARY_LOG"
  else
    printf '  \033[31mFAILED\033[0m  %d/%d tests (%d passed)\n' "$_FAIL" "$((_PASS+_FAIL))" "$_PASS"
    echo "  FAILED  $_FAIL/$((_PASS+_FAIL)) failed  [$perm_name]  $ts" >> "$SUMMARY_LOG"
    for r in "${_RESULTS[@]}"; do
      echo "$r" >> "$SUMMARY_LOG"
    done
  fi
  echo ""
}

# ── Permutation definitions ───────────────────────────────────────────────────

run_1() {
  local name="P1: ALB + tls=none"
  local ts; ts=$(date +%Y%m%d-%H%M%S)
  local logdir="$LOG_DIR/p1-alb-none-$ts"
  mkdir -p "$logdir"
  local log="$logdir/run.log"
  _reset_results

  _section "$name"

  _write_override 'tls_certificate_source  = "none"
enable_envoy_gateway    = false
enable_istio_gateway    = false
create_cert_manager_irsa = false'

  _helm_uninstall "$log"
  _tf_destroy "$log"
  _tf_apply "$log"
  _kubeconfig
  _helm_deploy "$log"

  # Assertions
  _step "Running assertions..."
  local alb_host
  alb_host=$(_tf output -raw alb_dns_name 2>/dev/null || echo "")
  _assert "ALB hostname from TF output" "$([[ -n "$alb_host" ]] && echo true || echo false)"

  _assert_http "GET http://${alb_host}/api/v1/health → 200" "http://${alb_host}/api/v1/health"
  _assert_http "GET http://${alb_host}/ → 200 (frontend)" "http://${alb_host}/" "200"

  # No TLS — ALB should not redirect
  local redirect
  redirect=$(curl -so /dev/null -w "%{http_code}" --max-time 10 "http://${alb_host}/" 2>/dev/null || echo "000")
  _assert "ALB: no HTTPS redirect (tls=none)" "$([[ "$redirect" != "301" && "$redirect" != "308" ]] && echo true || echo false)" "got $redirect"

  _assert_k8s_absent "No ClusterIssuer letsencrypt-prod" \
    "kubectl get clusterissuer letsencrypt-prod -o name 2>/dev/null"

  _print_results "$name" "$ts"
}

